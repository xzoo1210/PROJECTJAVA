    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        request.setCharacterEncoding("UTF-8");
        CustomerDAO userDB = new CustomerDAO();
        String userName = request.getParameter("userName_register");
        String password = request.getParameter("userPassword_register");
        String rePassword = request.getParameter("reUserPassword_register");

        Customer user = userDB.checkLogin(userName, password);
        if (user != null) {
            request.setAttribute("mess", "Tên đăng nhập đã tồn tại!");
            request.getRequestDispatcher("login_register.jsp").forward(request, response);
        } else {
            if (!password.equals(rePassword)) {
                request.setAttribute("mess", "Mật khẩu không trùng khớp!");
                request.getRequestDispatcher("login_register.jsp").forward(request, response);
            } else {
                int i = userDB.registerUser(userDB.getLastID() + 1, userName, password, 2);
                request.setAttribute("mess", "đăng kí thành công!");
                request.getRequestDispatcher("login_register.jsp").forward(request, response);
            }
        }
    }

